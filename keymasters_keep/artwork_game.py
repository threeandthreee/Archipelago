    def random_prompts(self) -> List[str]:
        valid_combinations: List[List[str]] = [
            ["ADJECTIVE", "NOUN"],
            ["NOUN", "NOUN"],
            ["VERB", "NOUN"],
        ]

        func_mapping: Dict[str, Callable[[], str]] = {
            "ADJECTIVE": self.words_adjectives,
            "NOUN": self.words_nouns,
            "VERB": self.words_verbs,
        }

        random_prompts: List[str] = list()

        for _ in range(3):
            combination = self.random.choice(valid_combinations)

            random_prompts.append(
                " ".join(self.random.choice(func_mapping[part]()) for part in combination)
            )

        return random_prompts


