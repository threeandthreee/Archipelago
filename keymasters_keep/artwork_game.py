@dataclass
class ArtworkArchipelagoOptions:
    artwork_actions: ArtworkActions
    artwork_subjects: ArtworkSubjects
    artwork_themes: ArtworkThemes
    artwork_art_styles: ArtworkArtStyles
    artwork_artists: ArtworkArtists
    artwork_include_random_prompts: ArtworkIncludeRandomPrompts


class ArtworkGame(Game):
    name = "Artwork"
    platform = KeymastersKeepGamePlatforms.META

    platforms_other = None

    options_cls = ArtworkArchipelagoOptions

    def optional_game_constraint_templates(self) -> List[GameObjectiveTemplate]:
        templates: List[GameObjectiveTemplate] = [
            GameObjectiveTemplate(
                label="Avoid using the following color groups: COLORS",
                data={
                    "COLORS": (self.color_groups, 3),
                },
            ),
            GameObjectiveTemplate(
                label="Try to work in the following color groups: COLORS",
                data={
                    "COLORS": (self.color_groups, 3),
                },
            ),
            GameObjectiveTemplate(
                label="Change something about your usual process / tools",
                data=dict(),
            ),
        ]

        if len(self.art_styles()):
            templates.append(
                GameObjectiveTemplate(
                    label="Try to work in the following art style: STYLE",
                    data={
                        "STYLE": (self.art_styles, 1),
                    },
                )
            )

        if len(self.artists()):
            templates.append(
                GameObjectiveTemplate(
                    label="Take inspiration from the work of ARTIST",
                    data={
                        "ARTIST": (self.artists, 1),
                    },
                )
            )

        return templates

    def game_objective_templates(self) -> List[GameObjectiveTemplate]:
        templates: List[GameObjectiveTemplate] = [
            GameObjectiveTemplate(
                label="ACTION something to the following specification:  Subject: SUBJECT  Theme: THEME",
                data={
                    "ACTION": (self.actions, 1),
                    "SUBJECT": (self.subjects, 1),
                    "THEME": (self.themes, 1),
                },
                is_time_consuming=True,
                is_difficult=True,
                weight=2,
            ),
        ]

        if self.include_random_prompts:
            templates.append(
                GameObjectiveTemplate(
                    label="ACTION something taking inspiration from one of the following prompts: PROMPTS",
                    data={
                        "ACTION": (self.actions, 1),
                        "PROMPTS": (self.random_prompts, 3),
                    },
                    is_time_consuming=True,
                    is_difficult=False,
                    weight=1,
                )
            )

        return templates

    def actions(self) -> List[str]:
        return sorted(self.archipelago_options.artwork_actions.value)

    def subjects(self) -> List[str]:
        return sorted(self.archipelago_options.artwork_subjects.value)

    def themes(self) -> List[str]:
        return sorted(self.archipelago_options.artwork_themes.value)

    def art_styles(self) -> List[str]:
        return sorted(self.archipelago_options.artwork_art_styles.value)

    def artists(self) -> List[str]:
        return sorted(self.archipelago_options.artwork_artists.value)

    @property
    def include_random_prompts(self) -> bool:
        return bool(self.archipelago_options.artwork_include_random_prompts.value)

